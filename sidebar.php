        <h2>Nieuws</h2>
        <p>Voor de zevende keer staat de Triathlon Langedijk in 2018 op de kalender! Deze editie wordt
            op woensdagavond 20 juni 2018 georganiseerd door Stichting
            Triathlon Langedijk in samenwerking met Triathlonvereniging
            Zwefilo. Vanaf vrijdag 3 februari 2018 om 00.00 uur kunnen sportievelingen zich
            inschrijven om mee te doen.
            Het is een laagdrempelig, recreatief evenement over de kortste
            triathlon afstanden circa 600m zwemmen, 18,4km fietsen en 5km lopen. Deelname is individueel mogelijk, maar
            ook als team
            van twee of drie atleten, waarbij een ieder één of twee van de
            onderdelen voor zijn&#47;haar rekening neemt. Dus iedereen die een
            beetje sportief is, kan meedoen! De start en finish van het
            evenement zijn bij Hotel Restaurant “De Buizerd” aan de
            Spoorstraat in Noord&ndash;Scharwoude. Hier is ook de wisselzone.</p>

        <h4>Publiek</h4>
        <p>Het zwemmen zal zijn direct voor De Buizerd in het kanaal
            Alkmaar&ndash;Kolhorn, daarna wordt er gefietst op een parcours aan de
            overzijde van het kanaal waarbij de deelnemers een viertal ronden
            moeten afleggen. Als laatste wordt er gelopen door de Spoorstraat
            en de Dorpsstraat in Oudkarspel met finish direct voor De
            Buizerd. Ook voor het publiek is het evenement prima te volgen,
            doordat de atleten meerdere malen voorbij komen. Dat bleek de
            afgelopen jaren toen het publiek in grote getale langs het
            parcours stond. De eerste start is om 19.30 uur.</p>

        <h4>Veel belangstelling</h4>
        <p>De animo voor deelname was de vorige edities groot. De
            individuele inschrijving is snel vol, dus wees er snel bij. Ook voor
            de deelname als team is veel belangstelling, ook een reden om
            direct in te schrijven. Je kunt deelnemen als gezin, vrienden of
            bedrijfsteam, waarbij niet alleen het winnen, maar vooral ook het
            meedoen en de gezelligheid</p>

